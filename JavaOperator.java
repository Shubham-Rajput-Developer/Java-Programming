import java.util.*;
public class JavaOperator {
    public static void main(String args[])
    {
        Scanner sc = new Scanner(System.in);
        sc.close();
        int a = 10;
        int b = 5;
        System.out.println(a==b);
        System.out.println(b!=a);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>=b);
        
    }
}
